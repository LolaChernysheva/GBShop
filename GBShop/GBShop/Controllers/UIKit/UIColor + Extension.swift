    static func mainDarkGrayColor() -> UIColor {
        #colorLiteral(red: 0.2, green: 0.2, blue: 0.2, alpha: 1)
    }
    static func mainWhiteColor() -> UIColor {
        #colorLiteral(red: 0.9254901961, green: 0.9254901961, blue: 0.9254901961, alpha: 1)
    }
    static func mainYellowColor() -> UIColor {
        #colorLiteral(red: 0.9607843137, green: 0.8039215686, blue: 0.4745098039, alpha: 1)
    }
    static func mainLightGrayColor() -> UIColor {
        #colorLiteral(red: 0.3176470588, green: 0.3176470588, blue: 0.3176470588, alpha: 1)
    }
}
