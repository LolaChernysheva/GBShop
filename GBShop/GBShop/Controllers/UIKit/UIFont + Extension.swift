import Foundation
import UIKit

extension UIFont {
    static func avenir15() -> UIFont? {
         UIFont.init(name: "Avenir", size: 15)
    }
    static func avenir30() -> UIFont? {
         UIFont.init(name: "Avenir", size: 30)
    }
    static func avenir20() -> UIFont? {
         UIFont.init(name: "Avenir", size: 20)
    }
}
